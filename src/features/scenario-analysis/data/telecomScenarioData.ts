/* ─── Helper: Get FCF data for a scenario ─── */
export function getFCFByScenario(
  scenario: "orderly" | "disorderly" | "hothouse",
): FCFYearData[] {
  switch (scenario) {
    case "orderly":
      return FCF_ORDERLY;
    case "disorderly":
      return FCF_DISORDERLY;
    case "hothouse":
      return FCF_HOTHOUSE;
    default:
      return FCF_ORDERLY;
  }
}

export function getNPVSummary(
  scenario: "orderly" | "disorderly" | "hothouse",
): NPVSummary | undefined {
  return NPV_SUMMARIES.find((s) => s.scenarioType === scenario);
}

